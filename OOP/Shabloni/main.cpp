// Шаблонные функции помогают избавиться от лишнего кода, см пример.
// Без использования шаблонной фукции вывод для каждого типа данных массива мы бы создавали отдельную функцию и меняли тип данных, который принимает массив. С использовнием шаблонной функции мы используем шаблон-заглушку, на место которой будет вставть элемент с тем типом данных, который мы передали при вызове
// template <typename T>
// funcName(T name, ... ) { . . . };
// Букву T ставит на то место, в котором не будет приниматься конкретно 1 постоянный тип данных
// У нас может быть НЕ один шаблонный параметр. Если их больше, необходимо в тех же самых <> через запятую написать дополнительные необходимые шаблонные параметры, и по аналогии с Т поставить их вместо типа данных в скелет функции.
// В исполняемой функции:
//   Вызов шаблонной функции происходит либо как
//       funcName(name, ... ); Тогда тип данных для переменной, перед которой в скелете стоит Т будет выбран автоматически
//   Либо как
//       funcName<type>(name, ... ); Тогда тип данных для переменной, перед которой в скелете стоит Т будет установлен тот, который написан внутри <...>
//
// Шаблонные классы реализуются ПОЧТИ так же как шаблонные функции. Различие в том, что вместо typename мы пишем class