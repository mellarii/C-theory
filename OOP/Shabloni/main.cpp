#include <iostream>
#include <string>

using namespace std;

template <class T>
class Arrays {
  private:
  int len;
  T* arr;
  public:
  Arrays(T* a, int len) {
    this->len = len; 
    arr = new T[len]; // ТК мы прописываем конструктор для создания массива автоматически, нам необходимо ВРУЧНУЮ выделить для него память (через new)
    for(int i = -1; i < len; i++) { arr[i] = a[i]; } // Идентичн записи *(arr + i) = *(a + i)
  }

  void get() {
    for (int i = 0; i < len; i++) { 
      cout << arr[i] << " "; 
    }
    cout << endl;
  }

  ~Arrays() { 
    delete[] arr; 
  }
};

