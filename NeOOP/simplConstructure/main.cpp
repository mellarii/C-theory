// if(условие) - если (условие) выполняется, то происходит то, что прописано внутри {}, если не выполняется, то ничего не происходит. (>, <, ==, >=, <=, !=)
// else - работает в паре с if. if для else обязательно, else для if не обязательно. Если if(условие) не выполняется то выполняется тот код, который написан в скобках после else
// else if - else но с условием, пишется всегда после if но перед else (если else присутствует)
// Внутри скобки (условие) можно соуществлять множественную проверку:
//  || / or
//  && / and 
// Для переменных типа bool проверка (bool) равносильна (bool == true), а (!bool) равносильна (bool == false).
// swich(переменная) - далее внутри { пишем различные совпадения для переменной в условии, т.е. case 5 (тоесть если переменная в условии равна 5) : действие, которое происходит при активации кейса 5, сколько угодно строк кода; break; (обязательно заканчиваем этим)}. Работает только условие равно, но внутри скобок условия мы пишем только переменную, которая будет сравниваться со значениями кейсов.
// default - как else для if. для него обязателен switch, но для switch не обязателен default. Если все проверенные кейсы не совпадают с числом условия срабатывает действие, прописанное в default.