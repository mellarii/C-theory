int main() {
  int num;
  std::cin >> num;
  switch(num) {
    case 1: std::cout << "Num is 1"; break;
    case 2: std::cout << "Num is 2"; break;
    case 3: std::cout << "Num is 3"; break;
    case 4: std::cout << "Num is 4"; break;
    case 5: std::cout << "Num is 5"; break;
    default: std::cout << "Default"; break;
  }
}

