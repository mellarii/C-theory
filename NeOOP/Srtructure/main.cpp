#include <iostream>
#include <string>

using namespace std;

struct Point {
  int x, y;
};

struct Tree {
  string name;
  int ages;
  bool is_alive;
  float height;
  Point place;

  void get_info() {
    cout << "Name: " << name << ". Ages: " << ages << endl;
  }
};

int main() {
  Tree dub;
  dub.name = "Дуб";
  dub.ages = 24;
  dub.place.x = 100;
  dub.place.y = 5;

  Tree klen;
  klen.name = "Клён";
  klen.ages = 3;
  dub.place.x = 20;
  dub.place.y = 4;

  dub.get_info();
  klen.get_info();

  return 0;
}
